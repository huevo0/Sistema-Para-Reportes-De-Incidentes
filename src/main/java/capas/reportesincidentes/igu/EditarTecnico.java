    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        Tecnico tecnico = control.traerUnTecnico(idTecnico);
        List<Servicio>listaServicios = tecnico.getEspecialidad();
        txtNombre.setText(tecnico.getNombre());
        if (listaServicios!=null){
            for (Servicio listaServicio : listaServicios) {
                cmbServicio.addItem(listaServicio.getNombre());
            }
        }
    }//GEN-LAST:event_formWindowOpened

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
          String nombre = txtNombre.getText();
          
          control.editarTecnico(idTecnico, nombre);

          RecursosHumanos recursosHumanos = new RecursosHumanos(control, idTecnico);
          recursosHumanos.mostrarMensaje("Tecnico guardado correctamente", "info", "Alta Exitosa");
          this.dispose();
          
    }//GEN-LAST:event_btnGuardarActionPerformed

