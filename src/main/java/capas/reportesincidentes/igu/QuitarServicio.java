    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        List<Servicio>listaServicios = control.traerServicios();
        Cliente cliente = control.traerUnCliente(idCliente);
        txtRazonSocial.setText(cliente.getRazonSocial());
        txtCuit.setText(cliente.getCuit());
        txtTelefono.setText(cliente.getTelefono());
        txtEmail.setText(cliente.getEmail());
        List<Servicio>servCliente = cliente.getServicios();
        
        
        if (listaServicios!=null){
            for (Servicio servicio : listaServicios) {
                for (Servicio scli : servCliente) {
                    if(servicio.getId()==scli.getId()){
                        cmbServicio.addItem(servicio.getNombre());
                    }
                }
            }
        }
    }//GEN-LAST:event_formWindowOpened

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if(cmbServicio.getSelectedIndex()!=0){  
            String servicio = (String) cmbServicio.getSelectedItem();
          
            control.quitarUnServicio(idCliente, servicio);
            mostrarMensaje("Cliente guardado correctamente", "info", "Edicion Exitosa");
            this.dispose();
        } else {  mostrarMensaje("Por favor verifique que los campos no esten vacios", "Error", "Error al Guardar");
            }
    }//GEN-LAST:event_btnGuardarActionPerformed

