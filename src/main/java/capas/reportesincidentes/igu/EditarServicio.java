    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
          String nombre = txtNombre.getText();
          String descripcion = (String) cmbTipo.getSelectedItem();
          
          
          control.editarServicio(idServicio, nombre, descripcion);
          AreaComercial areaComercial = new AreaComercial(this.control , this.idCliente);
          areaComercial.mostrarMensaje("El Servicio se creo correctamente", "info", "Guardado Exitoso");
          this.dispose();
    }//GEN-LAST:event_btnGuardarActionPerformed

